#include"calc.h"

double addition(double var1, double var2)
{
	return var1 + var2;
}

double subtraction(double var1, double var2)
{
	return var1 - var2;
}

double multiplication(double var1, double var2)
{
	return var1 * var2;
}

double division(double var1, double var2)
{
	return var1 / var2;
}

double secondDegree(double var1)
{
	return var1 * var1;
}